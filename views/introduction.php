<h1>Kelimeci ne işe yarar?</h1>
<p>Kelimeci, sözcük dağarcığınızı geliştirmenizi sağlayan araçtır.
Yeni öğrendiğiniz kelimeleri akıllı seviye sistemi ve çeşitli 
testlerle unutmamanızı sağlar. </p>
<p>Ek olarak; bir kelime öğrenirken birlikte bu kelimeyle ilişkili 
olanları da öğreterek akılda kalıcılığı ve doğru kullanımı sağlar.</p>
<p>Üye girişi yapıp, kelime dağarcığınızı geliştirmeye başlayın.</p>
